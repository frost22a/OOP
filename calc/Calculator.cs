using System;


namespace OOP
{
    class Calculator
    {
        public int Add(int first, int second)
        {
            return first + second;
        }
        public int Substract(int first, int second)
        {
            return first - second;
        }
        public int Mulitply(int first, int second)
        {
            return first * second;
        }
        public int Divide(int first, int second)
        {
            return first / second;
        }
    }
}
